#include <iostream>
using namespace std;

int main()
{
	int a, b;

	cin >> a >> b;

	int p = a + b;
	cout << p << endl;
	int m = a - b;
	cout << m << endl;
	int s = a * b;
	cout << s << endl;
	int n = a / b;
	cout << n << endl;
	int l = a % b;
	cout << l << endl;

	return 0;

}